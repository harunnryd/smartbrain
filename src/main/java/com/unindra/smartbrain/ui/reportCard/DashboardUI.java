    private void itemDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemDeleteActionPerformed
        int selectedRow = tableReportCard.getSelectedRow();
        
        if (selectedRow >= 0) {
            int id = (int) tableReportCard.getValueAt(selectedRow, 0);
            EntityManager entityManager = new EntityManager();
            IfaceReportCardRepository repo = new ReportCardRepository(entityManager);
            repo.deleteReportCardById(id);
            
            initTableReportCard();
        }
    }//GEN-LAST:event_itemDeleteActionPerformed

