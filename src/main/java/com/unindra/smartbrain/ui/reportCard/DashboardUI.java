    public void initTableReportCard() {
        ReportCardPageButton.setEnabled(false);
        
        tableModel = new DefaultTableModel();
        tableModel.addColumn("ID");        
        tableModel.addColumn("Nama Pengajar");
        tableModel.addColumn("Nama Peserta");        
        tableModel.addColumn("Nama Kursus");        
        tableModel.addColumn("Nilai");
        tableModel.addColumn("Tahun Ajaran");
        
        EntityManager entityManager = new EntityManager();
        IfaceReportCardRepository repo = new ReportCardRepository(entityManager);
        List<ReportCard> reportCards = repo.getReportCards();
        
        for (ReportCard reportCard : reportCards) {
            tableModel.addRow(new Object[]{reportCard.getId(), reportCard.getInstructorName(), reportCard.getParticipantName(), reportCard.getCourseName(), reportCard.getScore(), reportCard.getSchoolYear()});
        }
        
        tableReportCard.setModel(tableModel);
    }

